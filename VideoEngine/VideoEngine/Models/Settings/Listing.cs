namespace Jugnoon.Settings
{
    public class Listing
    {
        /// <summary>
        /// Toggle on | off views option in listing items unless you add customization manually.
        /// </summary>
        public bool enable_views { get; set; }

        /// <summary>
        /// Toggle on | off date option in listing items unless you add customization manually.
        /// </summary>
        public bool enable_date { get; set; }

        /// <summary>
        /// Toggle on | off username option in listing items unless you add customization manually.
        /// </summary>
        public bool enable_username { get; set; }

        /// <summary>
        /// Toggle on | off rating option in listing items unless you add customization manually.
        /// </summary>
        public bool enable_rating { get; set; }

        /// <summary>
        /// Toggle on | off like / dislike option in listing items unless you add customization manually.
        /// </summary>
        public bool enable_likedislike { get; set; }

    }
}


